

/*
Ejercicio 1:
Declara dos variables numéricas (con el valor que desees), muestra por consola la suma,
resta, multiplicación, división y módulo (resto de la división).

 */

public class Ejercicio1 {

    public static void main(String[] args) {
        int numero1 = 15; // asigna un valor a la primera variable
        int numero2 = 3; // asigna un valor a la segunda variable

        System.out.println(" suma : "+ (numero1 + numero2));  
        System.out.println(" resta : " + (numero1 - numero2));  
        System.out.println(" multiplicacion : "+(numero1 * numero2));  
        System.out.println(" division : "+(numero1 / numero2));  
        System.out.println( "resto de la división de las dos variables : " + (numero1 % numero2));  

    }
}
